package com.study.soulx.design_patterns.offcial.headfirsts.designpatterns.command.remoteWL;

public class Light {
	String location = "";

	public Light(String location) {
		this.location = location;
	}

	public void on() {
		System.out.println(location + " light is on");
	}

	public void off() {
		System.out.println(location + " light is off");
	}
}
